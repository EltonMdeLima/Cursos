#include <stdio.h>



int main() {

    // Laço for​

    for (int i = 0; i < 5; i++) {

        printf("For loop: %d\n", i);

    }



    // Laço while​

    int j = 0;

    while (j < 5) {

        printf("While loop: %d\n", j);

        j++;

    }



    // Laço do-while​

    int k = 0;

    do {

        printf("Do-while loop: %d\n", k);

        k++;

    } while (k < 5);



    return 0;

}